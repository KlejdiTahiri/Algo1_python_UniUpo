def integer_sort(dim, array):
    array_sup = [0] * (max(array) + 1)
    print(array_sup)
    for i in range(dim):
        index = array[i]
        array_sup[index] += 1
    j = 0
    for i in range(len(array_sup)):
        while array_sup[i] > 0:
            array[j] = i
            j += 1
            array_sup[i] -= 1


